public class Main {
    public static void main(String[] args) {

        int a = 0;
        a = a + 1;
        a++;
        a--;
        System.out.println(a);
        System.out.println(a++);
        System.out.println(a--);
        System.out.println(++a);
        System.out.println(--a);
    }
}